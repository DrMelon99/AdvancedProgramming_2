#include "games.h"

void mugunghwa(void)
{
	mugunghwa_init();

	dialog(0, -1);

	while (1)
	{
		if (player_control())
			break;

		npc_move_mugunghwa();

		// 영희의 "무궁화 꽃이 피었습니다" 말하기 및 상태 전환
		younghee();

		// 사용자 움직임 키입력
		key_t key = get_key();
		if ((key == K_QUIT) || (n_alive == 1) || (count > 3))
		{
			break;
		}
		else if (key != K_UNDEFINED)
		{
			player_control(key);
		}

		// 다른 NPC 랜덤 움직임 작동
		for (int i = 1; i < n_player; i++)
		{
			if (tick[0] % player[i].period == 0)
			{
				npc_move_mugunghwa(i, -1);
			}
		}

		display();
		Sleep(10);
		tick[0] += 10;
	}

	player_stamina(-1, 0, -1);
}

void mugunghwa_init(void)
{
	game_round = 1;

	system("cls");

	SetConsoleFontSize(20);
	system("mode con: cols=60 lines=40");

	map_init(15, 60);

	// 모든 플레이어 게임 통과 여부 false 초기 설정
	for (int i = 0; i < n_player; i++)
	{
		player[i].is_pass = false;
	}

	// "무궁화 꽃이 피었습니다" 라운드와 통과 플레이어 수 0으로 초기화
	count = pass_n_player = 0;


	// 영희 배치
	for (int i = 0; i < 3; i++)
	{
		back_buf[6 + i][1] = '#';
	}

	// n명의 플레이어들을 우측 시작 지점에 랜덤 배치
	int x, y;
	for (int i = 0; i < n_player; i++)
	{
		do
		{
			x = randint(1, N_ROW - 2);
			y = (N_COL - 2);
		} while (!placable(x, y));

		player[i].px = x;
		player[i].py = y;
		player[i].period = randint(100, 500);

		back_buf[player[i].px][player[i].py] = '0' + i;  // (0 .. n_player-1)
	}

	display();

	tick[0] = 0;
}